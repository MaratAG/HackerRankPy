""""HackerRank Maths 6 Integers Come In All sizes"""


def problem_solution(a, b, c, d):
    # Task function
    print(pow(a, b) + pow(c, d))


def main():
    # Initialization
    a = int(input())
    b = int(input())
    c = int(input())
    d = int(input())

    problem_solution(a, b, c, d)


if __name__ == '__main__':
    main()
