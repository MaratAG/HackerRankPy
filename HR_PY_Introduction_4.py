def problem_solution(a, b):
    # Task function
    print(a // b)
    print(a / b)


def main():
    # Initialization
    a = 0
    b = 0

    while (a == 0):
        a = int(input())

    while (b == 0):
        b = int(input())

    problem_solution(a, b)


main()
